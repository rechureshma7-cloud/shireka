export function filterProducts(
  products: MockProduct[],
  filters: {
    gender?: string;
    color?: string;
    occasion?: string;
    category?: string;
    platform?: string;
  },
): MockProduct[] {
  return products.filter((p) => {
    if (filters.gender && p.gender !== filters.gender) return false;
    if (filters.color && p.color.toLowerCase() !== filters.color.toLowerCase())
      return false;
    if (filters.occasion && p.occasion !== filters.occasion) return false;
    if (filters.category && p.category !== filters.category) return false;
    if (filters.platform && p.platform !== filters.platform) return false;
    return true;
  });
}

export function getAISuggestion(
  members: Array<{ gender: string; color: string; occasion: string }>,
): string {
  if (members.length === 0) return "";
  const colors = members.map((m) => m.color);
  const allSame = colors.every((c) => c === colors[0]);
  const occasion = members[0]?.occasion || "";

  const complementaryPairs: Record<string, string[]> = {
    Pink: ["Gold", "Maroon", "White", "Pink"],
    Blue: ["Gold", "White", "Maroon", "Blue"],
    Maroon: ["Gold", "Pink", "White", "Maroon"],
    Yellow: ["Green", "Orange", "White", "Yellow"],
    Gold: ["Maroon", "Blue", "Pink", "Gold"],
  };

  const isComplementary = colors.every(
    (c) => complementaryPairs[colors[0]]?.includes(c) ?? false,
  );

  if (allSame) {
    return `Perfect color coordination! Your ${colors[0]} twinning outfits create a stunning unified look${occasion ? ` perfect for ${occasion}` : ""}.`;
  }
  if (isComplementary) {
    return `Beautiful complementary harmony! The ${colors.join(" & ")} combination creates a visually balanced and elegant family look${occasion ? ` for ${occasion}` : ""}.`;
  }
  return `Eclectic style! Your mix of ${colors.join(", ")} colors celebrates individuality while maintaining a cohesive family aesthetic${occasion ? ` for ${occasion}` : ""}.`;
}
